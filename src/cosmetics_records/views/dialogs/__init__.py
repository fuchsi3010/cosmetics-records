# Cosmetics Records - Dialogs Package
# =============================================================================
# This package contains modal dialog windows for data entry and editing.
#
# All dialogs inherit from BaseDialog which provides:
#   - Modal behavior (blocks interaction with main window)
#   - Semi-transparent overlay background
#   - Escape key to close
#   - Enter key to submit
#   - Click outside to close
#
# Dialogs:
#   - AddClientDialog / EditClientDialog
#   - AddTreatmentDialog / EditTreatmentDialog
#   - AddProductRecordDialog / EditProductRecordDialog
#   - AddInventoryDialog / EditInventoryDialog
