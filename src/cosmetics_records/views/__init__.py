# =============================================================================
# Cosmetics Records - Views Package
# =============================================================================
# This package contains all PyQt6 user interface components (View in MVC).
#
# Subpackages:
#   - components/: Reusable UI widgets (navbar, search bar, etc.)
#   - dialogs/: Modal dialog windows
#   - clients/: Client list and detail views
#   - inventory/: Inventory management view
#   - audit/: Audit log viewer
#   - settings/: Application settings view
#
# Views are responsible for:
#   - Displaying data to the user
#   - Capturing user input
#   - Emitting signals to notify controllers of user actions
